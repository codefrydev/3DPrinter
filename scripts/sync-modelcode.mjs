import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const modelcodeDir = path.join(root, "modelcode");
const outDir = path.join(modelcodeDir, "generated");
const codeMapFile = path.join(outDir, "codeMap.ts");

function toModuleBasename(pyName) {
  return pyName.replace(/\.py$/i, "");
}

function emitCodeMap(rows) {
  const header = `/* Auto-generated by scripts/sync-modelcode.mjs — do not edit */\n`;
  const props =
    rows.length === 0
      ? ""
      : rows
          .map(
            ({ base, text }) =>
              `  ${JSON.stringify(base)}: ${JSON.stringify(text)}`,
          )
          .join(",\n");
  const body = `const _: Record<string, string> = {\n${props}\n};\nexport default _;\n`;
  fs.writeFileSync(codeMapFile, header + body, "utf8");
}

function clearGeneratedTs() {
  if (!fs.existsSync(outDir)) return;
  for (const name of fs.readdirSync(outDir)) {
    if (name.endsWith(".ts")) {
      fs.unlinkSync(path.join(outDir, name));
    }
  }
}

function main() {
  if (!fs.existsSync(modelcodeDir)) {
    console.warn("sync-modelcode: modelcode/ missing, skipping.");
    return;
  }

  fs.mkdirSync(outDir, { recursive: true });
  clearGeneratedTs();

  const entries = fs.readdirSync(modelcodeDir, { withFileTypes: true });
  const rows = [];
  for (const ent of entries) {
    if (!ent.isFile() || !ent.name.endsWith(".py")) continue;
    const abs = path.join(modelcodeDir, ent.name);
    const text = fs.readFileSync(abs, "utf8");
    const base = toModuleBasename(ent.name);
    rows.push({ base, text });
  }

  emitCodeMap(rows);

  if (rows.length === 0) {
    console.warn("sync-modelcode: no .py files found under modelcode/");
  } else {
    console.log(
      `sync-modelcode: bundled ${rows.length} script(s) into modelcode/generated/codeMap.ts`,
    );
  }
}

main();
